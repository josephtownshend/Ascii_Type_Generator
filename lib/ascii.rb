class Ascii_generator

  def letter_matcher(user_input)
    if user_input == "A"
      print [
        "//////////",
        "//////////",
        "////  ////",
        "////  ////",
        "//////////",
        "//////////",
        "////  ////",
        "////  ////",
        "////  ////",
        "////  ////"
      ].join("\n")
    elsif user_input == "B"
      print [
        "//////////",
        "//////////",
        "////  ////",
        "////  ////",
        "//////////",
        "//////////",
        "////  ////",
        "////  ////",
        "//////////",
        "//////////"
      ].join("\n")
    elsif user_input == "C"
      print [
        "//////////",
        "//////////",
        "////      ",
        "////      ",
        "////      ",
        "////      ",
        "////      ",
        "////      ",
        "//////////",
        "//////////"
      ].join("\n")
    else
      return nil
    end
  end

end
